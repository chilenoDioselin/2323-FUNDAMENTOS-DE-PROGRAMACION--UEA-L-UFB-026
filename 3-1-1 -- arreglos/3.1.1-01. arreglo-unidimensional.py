# Declaring a 1D array for daily temperatures
daily_temperatures = [75.2, 78.6, 82.1, 79.8, 85.3, 88.0, 87.5]

# Printing the temperatures
print("Daily Temperatures:", daily_temperatures)

# Accessing and printing a specific day's temperature
day = 3  # Zero-based indexing
temperature = daily_temperatures[day]
print("Temperature on Day", day + 1, ":", temperature)